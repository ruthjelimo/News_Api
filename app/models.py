class News:


    def __init__(self, source_name, author, title, description, url, urlToImage, publishedAt, content):

        
        self.source_name = source_name
        self.author = author
        self.title = title
        self.description = description
        self.url = url
        self.urlToImage = urlToImage
        self.publishedAt = publishedAt
        self.content = content